    private boolean isValidDate(String date) {
        dateFormat.setLenient(false);
        try {
            dateFormat.parse(date);
            return true;
        } catch (Exception e) {
            return false;
        }
    }
    
    private boolean isValidTime(String time) {
        // Validate time format
        if (!time.matches("\\d{2}:\\d{2}(AM|PM|am|pm)")) {
            return false; // Invalid format
        }
        return true;
    }
    
    private boolean isDuplicateName(String name) {
        try {
            List<String> lines = Files.readAllLines(Paths.get("storage/appointment.txt"));
            for (String line : lines) {
                // Split the line to extract the appointment name
                String[] parts = line.split(",");
                String existingName = parts[0]; // Assuming the name is the first column

                // Check if the name already exists
                if (existingName.equals(name)) {
                    return true; // Found a duplicate name
                }
            }
        } catch (IOException e) {
            e.printStackTrace(); // Handle or log the error
        }
        return false; // No duplicate names found
    }
    
    private void comfirmpaymentbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comfirmpaymentbuttonActionPerformed

        String name = apptNameField.getText();
        String date = apptDateField.getText();
        String time = apptTimeField.getText();
        String feedback = apptFeedbackField.getText();
        String amount = apptAmountField.getText();
        String paymentStatus = (String) apptPaymentStatusField.getSelectedItem();
        String selectedServiceType = (String) apptServiceTypeField.getSelectedItem();
        String technicianName = cbxAddApptTechnician.getSelectedItem().toString();
        String customerName = cbxAddApptCustomerName.getSelectedItem().toString();
        System.out.println(technicianName);
        System.out.println(customerName);
        
        if(name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Appointment name cannot be empty");
            return;
        }
        
        if (isDuplicateName(name)) {
            JOptionPane.showMessageDialog(this, "An appointment with the same name already exists");
            return;
        }
        
        if(amount.isEmpty() || !amount.chars().allMatch( Character::isDigit) ||
                Double.parseDouble(amount) <= 0) {
            JOptionPane.showMessageDialog(this, "Please enter a valid amount for the item");
            return;
        }
        
        if (date.isEmpty() || !date.matches("\\d{4}-\\d{2}-\\d{2}") || !isValidDate(date)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid date for the appointment");
            return;
        }
        
        if (time.isEmpty() || !isValidTime(time)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid time for the appointment");
            return;
        }
        
        if(customerName.isEmpty() ) {
            customerName = null;
            
        }
        
        if(technicianName.isEmpty() ) {
            technicianName = null;
            
        }
        
        if(feedback.isEmpty() ) {
            feedback = null;
            
        }
        
        Appt appt = new Appt(name,date,time,selectedServiceType,Double.parseDouble(apptAmountField.getText()),paymentStatus,technicianName,customerName,feedback);
        
        apptService.create(appt);
      
        apptNameField.setText("");
        apptFeedbackField.setText("");
        apptAmountField.setText("");
        apptServiceTypeField.setSelectedItem("");
        apptDateField.setText("");
        apptTimeField.setText("");
        apptPaymentStatusField.setSelectedItem("");
        JOptionPane.showMessageDialog(this, "Appointment has been added");
    }//GEN-LAST:event_comfirmpaymentbuttonActionPerformed

